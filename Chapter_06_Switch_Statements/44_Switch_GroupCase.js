let browser = "Edge";
switch (browser) {
    case "Chrome":
    case "Firefox":
        console.log("You are using a popular browser.");
        break;  
    case "Edge":
        console.log("You are using Microsoft Edge.");
        break;
    case "Safari":
        console.log("You are using Safari.");
        break;      
    default:
        console.log("Unknown browser.");
}